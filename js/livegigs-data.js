// 修复Logo链接 - 确保指向正确的首页
function fixLogoLinks() {
    const logo = document.getElementById('logo') || document.querySelector('.logo-img');
    if (logo) {
        // 检测当前页面类型
        const path = window.location.pathname;
        let homeUrl = './index.html';

        // 如果是cn页面，logo指向cn.html
        if (path.includes('cn')) {
            homeUrl = './cn.html';
        }

        // 移除原有的onclick事件
        logo.removeAttribute('onclick');

        // 添加新的点击事件 - 跳转到首页
        logo.style.cursor = 'pointer';
        logo.addEventListener('click', function(e) {
            e.preventDefault();
            window.location.href = homeUrl;
        });

        console.log('Logo link fixed to:', homeUrl);
    }
}

// 主加载函数
async function initLiveGigsData() {
    // 检测当前页面
    const path = window.location.pathname;
    let pageType = 'index';

    if (path.includes('cn')) pageType = 'cn';
    else if (path.includes('events')) pageType = 'events';
    else if (path.includes('partners')) pageType = 'partners';
    else if (path.includes('privacypolicy')) pageType = 'privacypolicy';
    else if (path.includes('accessibility')) pageType = 'accessibilitystatement';

    const config = DATA_CONFIG.pages[pageType];
    if (!config) return;

    // 修复Logo链接（所有页面都需要）
    fixLogoLinks();

    // 加载底部数据（所有页面都需要）
    if (config.footer) {
        const footerData = await loadJSON(config.footer);
        renderFooter(footerData);
    }

    // 加载其他数据...
    console.log(`LiveGigs Data initialized for ${pageType}`);
}

// 页面加载完成后初始化
document.addEventListener('DOMContentLoaded', initLiveGigsData);

// 每小时自动刷新数据
setInterval(initLiveGigsData, 3600000);
