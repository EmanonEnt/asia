// 从GitHub JSON文件加载数据并渲染到页面

const DATA_CONFIG = {
    baseUrl: 'https://emanonent.github.io/asia/content',
    version: Date.now(), // 防止缓存

    // 页面映射
    pages: {
        'index': { posters: 'posters-index', banner: 'banners', footer: 'footer-global' },
        'cn': { posters: 'posters-cn', banner: 'banners', footer: 'footer-cn' },
        'events': { posters: 'posters-events', carousel: 'carousel', events: 'events', footer: 'footer-global' },
        'partners': { banners: 'partners-banners', collaborators: 'collaborators', footer: 'footer-global' },
        'privacypolicy': { footer: 'footer-global' },
        'accessibilitystatement': { footer: 'footer-global' }
    }
};

// 加载JSON数据
async function loadJSON(filename) {
    try {
        const response = await fetch(`${DATA_CONFIG.baseUrl}/${filename}.json?v=${DATA_CONFIG.version}`);
        if (!response.ok) throw new Error('Not found');
        return await response.json();
    } catch (e) {
        console.warn(`Failed to load ${filename}:`, e);
        return null;
    }
}

// 渲染底部区域
function renderFooter(data) {
    if (!data) return;

    // 版权文字
    const copyrightEl = document.getElementById('footer-copyright') || document.querySelector('.copyright');
    if (copyrightEl && data.copyright) {
        copyrightEl.textContent = data.copyright;
    }

    // 社交链接
    const socialContainer = document.getElementById('footer-social') || document.getElementById('socialContainer');
    if (socialContainer && data.social) {
        socialContainer.innerHTML = data.social.map(item => {
            const iconSvg = getSocialIcon(item.icon);
            return `<a href="${item.url || '#'}" target="_blank" rel="noopener" class="social-link" title="${item.name || ''}">
                ${iconSvg}
            </a>`;
        }).join('');
    }

    // 制作单位Logo
    const producerEl = document.getElementById('footer-producer') || document.querySelector('.producer-logo');
    if (producerEl && data.producer) {
        if (producerEl.tagName === 'IMG') {
            producerEl.src = data.producer;
        } else {
            producerEl.innerHTML = `<img src="${data.producer}" alt="EMANON ENT" style="height: 35px; opacity: 0.8;">`;
        }
    }
}

